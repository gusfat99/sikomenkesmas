package com.example.sikomen_kesmas;

public class DataKlinik {
    private String namaKlinik;
    private String fotoKlinik;
    private String jadwalPraktik;
    private String lokasiKlinik;
    private String statusKlinik;

    public String getNamaKlinik() {
        return namaKlinik;
    }

    public void setNamaKlinik(String namaKlinik) {
        this.namaKlinik = namaKlinik;
    }

    public String getFotoKlinik() {
        return fotoKlinik;
    }

    public void setFotoKlinik(String fotoKlinik) {
        this.fotoKlinik = fotoKlinik;
    }

    public String getJadwalPraktik() {
        return jadwalPraktik;
    }

    public void setJadwalPraktik(String jadwalPraktik) {
        this.jadwalPraktik = jadwalPraktik;
    }

    public String getLokasiKlinik() {
        return lokasiKlinik;
    }

    public void setLokasiKlinik(String lokasiKlinik) {
        this.lokasiKlinik = lokasiKlinik;
    }

    public String getStatusKlinik() {
        return statusKlinik;
    }

    public void setStatusKlinik(String statusKlinik) {
        this.statusKlinik = statusKlinik;
    }
}
